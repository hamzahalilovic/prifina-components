export const getInfo = () => {
  //return ["queryOuraDaily", "queryOuraHourly", "queryNetflixData"];
  return [
    "queryRideTypes",
    "queryDriverETA",
    "queryRideEstimates",
    "queryNearbyDrivers"
  ];
};

export const getModuleName = () => {
  return "Lyft";
};
export const getFields = (query) => {
  let fields = [];
  switch (query) {
    case "queryRideTypes":
      fields = Object.keys(rideTypes);
      break;
    case "queryDriverETA":
      fields = Object.keys(driverETA);
      break;
    case "queryRideEstimates":
      fields = Object.keys(rideEstimates);
      break;
    case "queryNearbyDrivers":
      fields = Object.keys(nearby_drivers);
      break;
    default:
      fields = [];
  }
  return fields;
};

export const queryRideTypes = ({
  stage,
  appID,
  name,
  createQuery,
  fields,
  filter,
  next,
  fieldsList,
}) => {
  if (stage === "dev") {
    return Promise.resolve({
      data: {
        getDataObject: { content: rideTypes },
      },
    });
  } else {
    //SELECT * FROM s3object s  where EXTRACT(YEAR FROM TO_TIMESTAMP(s.p_datetime))=2021 LIMIT 5
    //https://docs.aws.amazon.com/AmazonS3/latest/userguide/s3-glacier-select-sql-reference-select.html

    return createQuery({
      query: dataQuery,
      name: name,
      fields,
      filter,
      next,
      appId: appID,
      fieldsList: fieldsList,
    });
  }
};

